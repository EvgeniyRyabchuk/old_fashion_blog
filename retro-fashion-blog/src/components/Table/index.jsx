const Td = ({ col, row, index }) => {
    console.log('td')
    const getContent = () => {
        const content = row[col.key];
        const type = col.type;



        if(type === "img")
            return <img src={content} width={100} height={100} />

        if(type === "html")
            return <HtmlContent html={content}/>

        if(type === "largeText")
            return <div className="td-content">{content}</div>

        if(type === "text" || !type || type === "" || type === "actions")
            return content;
    }

    return (
        <td >
            {getContent()}
        </td>
    )
}

